const mapNewIcons = ( dataset: Dataset ):Dataset => {
  const set:Dataset = {
    oldIcon: dataset.oldIcon,
    oldIconName: dataset.oldIconName,
  }
  if( iconMap.hasOwnProperty( dataset.oldIconName ) ) {
    set.icon = iconMap[ dataset.oldIconName ].templateResult;
    set.iconName = iconMap[ dataset.oldIconName ].iconName;
    set.iconNameDasherized = iconMap[ dataset.oldIconName ].iconNameDasherized;
    set.differentNamingConvention = iconMap[ dataset.oldIconName ].differentNamingConvention;
    set.formChanged = iconMap[ dataset.oldIconName ].formChanged;
    set.new = iconMap[ dataset.oldIconName ].new;
    set.deleted = iconMap[ dataset.oldIconName ].deleted;
    set.comment = iconMap[ dataset.oldIconName ].comment || '';
  }
    // newIcon: dataset.oldIconName === 'Arrow Back' ? html`${ unsafeSVG( newIcons[ './assets/icons/arrow-left.svg']()) }` : null,

  return set;
}

